/**
 * Helper - Durstenfeld Shuffle
 * https://stackoverflow.com/a/12646864/3068136
 */
export const shuffleArray = (array) => {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1))
        const temp = array[i]
        array[i] = array[j]
        array[j] = temp
    }
}